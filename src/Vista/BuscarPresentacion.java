       public void CentrarVentana(JInternalFrame internalFrame) {
        int x = (frmPrincipal.tbn_escritorio.getWidth() / 2) - internalFrame.getWidth() / 2;
        int y = (frmPrincipal.tbn_escritorio.getHeight() / 2) - internalFrame.getHeight() / 2;
        if (internalFrame.isShowing()) {
            internalFrame.setLocation(x, y);
        } else {
            frmPrincipal.tbn_escritorio.add(internalFrame);
            internalFrame.setLocation(x, y);
            internalFrame.show();
        }
    } 
    void mostrarPresentacion(String buscar) {
        String[] titulos = {"C√≥digo", "Descripci√≥n", "Estado"};
        String[] Registros = new String[3];
        modelo = new DefaultTableModel(null, titulos);
        String Sql = "SELECT * FROM presentacion WHERE Estado='Activo' AND CONCAT(IdPresentacion, Descripcion, Estado) LIKE '%" + buscar + "%'";

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(Sql);
            while (rs.next()) {
                Registros[0] = rs.getString("IdPresentacion");
                Registros[1] = rs.getString("Descripcion");
                Registros[2] = rs.getString("Estado");
                modelo.addRow(Registros);
            }
            tbPresentacion.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(BuscarPresentacion.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

