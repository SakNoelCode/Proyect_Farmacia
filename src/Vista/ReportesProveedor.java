     void mostrartodosEmpleados(String Buscar)
    {
        DefaultTableModel tabla= new DefaultTableModel();
        String []titulos={"Id","Nombre","Dni","Ruc","Dirección","Email","Teléfono","Banco","Cuenta","Estado",};
        tabla.setColumnIdentifiers(titulos);
        this.tbProveedor.setModel(tabla);
        String consulta= "SELECT * FROM proveedor WHERE CONCAT(Dni, Ruc, Nombre) LIKE '%" + Buscar + "%'";
        String []Datos= new String [10];
        try {
            Statement st = cn.createStatement();
            ResultSet rs= st.executeQuery(consulta);
            while(rs.next())
            {
                Datos[0]=rs.getString("IdProveedor");
                Datos[1]=rs.getString("Nombre");
                Datos[2]=rs.getString("Dni");
                Datos[3]=rs.getString("Ruc");
                Datos[4]=rs.getString("Direccion");
                Datos[5]=rs.getString("Email");
                Datos[6]=rs.getString("Telefono");
                Datos[7]=rs.getString("Banco");
                Datos[8]=rs.getString("Cuenta");
                Datos[9]=rs.getString("Estado");
                tabla.addRow(Datos);
            }
             tbProveedor.setModel(tabla);
            txtCantidad.setText(""+tbProveedor.getRowCount());
        } catch (SQLException ex) {
            Logger.getLogger(ReportesProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }          
    }
